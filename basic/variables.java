public class variables {
    
static void main(){
    // create , declare 
  int age;
  age = 10;
  age = 11;
  System.out.println(age);

  // define or initialize
  int totalMarks = 100;
  System.out.println(totalMarks);


//case sensitive
  int myAge = 10;
  int MyAge = 20;
  System.out.println("myAge: " + myAge);
  System.out.println("MyAge: " + MyAge);

  // starting letter 
  int marks = 100;
  int Marks= 200;
  int _marks = 300;
  int $marks = 400;
System.out.println("marks: " + marks);
System.out.println("Marks: " + Marks);
System.out.println("_marks: " + _marks);
System.out.println("$marks: " + $marks);        
  // subsequet characters
  int height_ = 130;
  int height$ = 140;
  int height1 = 150;
  int height$1 = 160;
 System.out.println("height_: " + height_);
 System.out.println("height$: " + height$);     
 System.out.println("height1: " + height1);
 System.out.println("height$1: " + height$1);       
  // no reserved keywords
  //int class = 10;

}
}
